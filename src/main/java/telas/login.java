    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     /**
     * Criamos um objeto do tipo Usuario
     */          

                              usuario usuariovalidar = new usuario();
                              usuario usuarioencontrado = new usuario();
                                 /**
     * Atribuimos os valores Login e Senha baseado nos dados dos componentes JTextField
     */      
                         
                              usuariovalidar.setLogin(TxtLogin.getText());
                            //  usuariovalidar.setSenha(Criptografia.getMD5(TxtSenha.getText()));  
                            usuariovalidar.setSenha(TxtSenha.getText());  
        usuarioencontrado = UsuarioDAO.validarUsuario(usuariovalidar);
            String TipoPermissao = usuarioencontrado.getTipo();
            if (usuarioencontrado != null) {
                                  JOptionPane.showMessageDialog(null, "Olá USUÁRIO, sua permissão é de " + TipoPermissao+"  Seja bem-vindo");
                                  if(usuarioencontrado.getTipo().equalsIgnoreCase("admin'"))
                                  {
                                        JOptionPane.showMessageDialog(null, "Usuário tipo administrador logado");
                                  }
                                    if(usuarioencontrado.getTipo().equalsIgnoreCase("operador"))
                                  {
                                        JOptionPane.showMessageDialog(null, "Usuário tipo operador logado");
                                  }
                                      if(usuarioencontrado.getTipo().equalsIgnoreCase("usuario'"))
                                  {
                                        JOptionPane.showMessageDialog(null, "Usuário tipo usuario logado");
                                  }
                                     
                                 Inicio_tela inicio = new Inicio_tela();
                             inicio.setVisible(true);
                       
                                  dispose();
                              } else {
                                  JOptionPane.showMessageDialog(null, "Não foi encontrado nenhum usuario com este login e senha.");
                              }
                             
             
    }//GEN-LAST:event_jButton1ActionPerformed

